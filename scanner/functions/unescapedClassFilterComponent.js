function unescapedClassFilterComponent(c)
{
    return ((c >= 65 && c <= 90) || (c >= 97 && c <= 122) ||
            (c >= 48 && c <= 57) || (c >= 39 && c <= 42)  ||
            c === 45 || c === 95 || c === 46 || c === 33  ||
            c === 126 || c === 35 || c === 59 || c === 47 ||
            c === 63 || c === 58 || c === 64 || c === 38  ||
            c === 61 || c === 43 || c === 36 || c === 44)
}